class Solution {
public:
    struct node *reverseIt(struct node *head, int k) {
        if (!head || !head->next || k == 1) return head;
        
        node *current = head;
        node *next_node = NULL;
        node *prev = NULL;
        int count = 0;
        
        node *check = current;
        int nodes_left = 0;
        while (check && nodes_left < k) {
            nodes_left++;
            check = check->next;
        }
        
        k = nodes_left < k ? nodes_left : k;
        
        while (current && count < k) {
            next_node = current->next;
            current->next = prev;
            prev = current;
            current = next_node;
            count++;
        }
        
        head->next = next_node ? reverseIt(next_node, k) : NULL;
        
        return prev;
    }
};


