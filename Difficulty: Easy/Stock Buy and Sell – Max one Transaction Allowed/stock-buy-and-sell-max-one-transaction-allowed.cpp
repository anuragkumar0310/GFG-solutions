class Solution {
public:
    int maximumProfit(vector<int>& prices) {
        int minimumPrice = INT_MAX;
        int maximumProfit = 0;
        int currentIndex = 0;
        
        while(currentIndex < prices.size()) {
            minimumPrice = (prices[currentIndex] < minimumPrice) ? prices[currentIndex] : minimumPrice;
            maximumProfit = (prices[currentIndex] - minimumPrice > maximumProfit) ? prices[currentIndex] - minimumPrice : maximumProfit;
            currentIndex++;
        }
        
        return maximumProfit;
    }
};



