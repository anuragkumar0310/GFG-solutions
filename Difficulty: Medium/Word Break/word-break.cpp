class Solution {
public:
    int wordBreak(int n, string s, vector<string>& dictionary) {
        unordered_set<string> dict(dictionary.begin(), dictionary.end());
        int len = s.length();
        vector<bool> dp(len + 1, false);
        dp[0] = true;
        int i = 1;
        while (i <= len) {
            int j = 0;
            while (j < i) {
                dp[i] = dp[j] && dict.find(s.substr(j, i - j)) != dict.end() ? true : dp[i];
                j++;
            }
            i++;
        }
        return dp[len] ? 1 : 0;
    }
};



