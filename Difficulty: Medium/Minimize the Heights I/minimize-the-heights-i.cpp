class Solution {
public:
    int getMinDiff(int k, vector<int> &arr) {
        int n = arr.size();
        sort(arr.begin(), arr.end());
        int ans = arr[n-1] - arr[0];
        
        int i = 0;
        while(i < n-1) {
            int mx = (arr[n-1] - k > arr[i] + k) ? arr[n-1] - k : arr[i] + k;
            int mn = (arr[i+1] - k < arr[0] + k) ? arr[i+1] - k : arr[0] + k;
            ans = (ans < abs(mx-mn)) ? ans : abs(mx-mn);
            i++;
        }
        return ans;
    }
};






